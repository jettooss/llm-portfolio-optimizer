def parse_json_payload_from_text(text: str) -> Any | None:
    cleaned = text.strip()
    fenced = re.search(r"```(?:json)?\s*(.*?)\s*```", cleaned, flags=re.DOTALL | re.IGNORECASE)
    if fenced:
        cleaned = fenced.group(1).strip()

    for candidate in (cleaned,):
        try:
            payload = json.loads(candidate)
            return payload if isinstance(payload, (dict, list)) else None
        except json.JSONDecodeError:
            pass

    starts = [(pos, char) for char in ("{", "[") if (pos := cleaned.find(char)) >= 0]
    if not starts:
        return None
    start, opener = min(starts)
    closer = "}" if opener == "{" else "]"

    if start < 0:
        return None

    depth = 0
    in_string = False
    escape = False
    for pos in range(start, len(cleaned)):
        char = cleaned[pos]
        if in_string:
            if escape:
                escape = False
            elif char == "\\":
                escape = True
            elif char == '"':
                in_string = False
            continue
        if char == '"':
            in_string = True
        elif char == opener:
            depth += 1
        elif char == closer:
            depth -= 1
            if depth == 0:
                try:
                    payload = json.loads(cleaned[start : pos + 1])
                    return payload if isinstance(payload, (dict, list)) else None
                except json.JSONDecodeError:
                    return None
    return None


def build_gigachat_model(model: str | None = None) -> Any:
    if model:
        os.environ["GIGACHAT_MODEL"] = normalize_gigachat_model_name(model)
    return build_model()


def invoke_json_chain(llm: Any, prompt: str, system_prompt: str = SYSTEM_PROMPT) -> PortfolioCandidateBatch | None:
    global LAST_LLM_DEBUG
    LAST_LLM_DEBUG = {
        "stage": "start",
        "error_type": "",
        "error_message": "",
        "raw_text_preview": "",
        "json_found": False,
    }
    try:
        from langchain_core.output_parsers import StrOutputParser  # type: ignore
        from langchain_core.prompts import ChatPromptTemplate  # type: ignore
    except ImportError as exc:
        LAST_LLM_DEBUG.update(
            {
                "stage": "import_langchain_core",
                "error_type": type(exc).__name__,
                "error_message": str(exc),
            }
        )
        return None
    try:
        prompt_template = ChatPromptTemplate.from_messages([("system", system_prompt), ("human", "{task_json}")])
        chain = prompt_template | llm | StrOutputParser()
        text = chain.invoke({"task_json": prompt})
        parsed_json = parse_json_payload_from_text(text)
        LAST_LLM_DEBUG.update(
            {
                "stage": "parse_response",
                "raw_text_preview": text[:1000],
                "json_found": parsed_json is not None,
            }
        )
        batch = parse_candidate_batch(parsed_json)
        if batch is None:
            LAST_LLM_DEBUG.update(
                {
                    "stage": "validate_response",
                    "error_type": "InvalidCandidateBatch",
                    "error_message": candidate_batch_validation_message(normalize_candidate_batch_payload(parsed_json))
                    or "Response JSON did not match PortfolioCandidateBatch",
                }
            )
        return batch
    except Exception as exc:
        LAST_LLM_DEBUG.update(
            {
                "stage": "invoke_model",
                "error_type": type(exc).__name__,
                "error_message": str(exc),
            }
        )
        return None


def call_langchain(prompt: str, model: str, base_url: str | None, temperature: float) -> PortfolioCandidateBatch | None:
    global LAST_LLM_DEBUG
    if is_gigachat_model(model):
        try:
            return invoke_json_chain(build_gigachat_model(model), prompt, system_prompt=SYSTEM_PROMPT_RU)
        except Exception as exc:
            LAST_LLM_DEBUG = {
                "stage": "build_gigachat_model",
                "error_type": type(exc).__name__,
                "error_message": str(exc),
                "raw_text_preview": "",
                "json_found": False,
            }
            return None

    if not os.getenv("OPENAI_API_KEY"):
        LAST_LLM_DEBUG = {
            "stage": "missing_openai_api_key",
            "error_type": "MissingOpenAIAPIKey",
            "error_message": "OPENAI_API_KEY is not set",
            "raw_text_preview": "",
            "json_found": False,
        }
        return None
    try:
        from langchain_openai import ChatOpenAI  # type: ignore
    except ImportError as exc:
        LAST_LLM_DEBUG = {
            "stage": "import_langchain_openai",
            "error_type": type(exc).__name__,
            "error_message": str(exc),
            "raw_text_preview": "",
            "json_found": False,
        }
        return None
    try:
        llm = ChatOpenAI(
            model=model,
            temperature=temperature,
            base_url=base_url,
            timeout=float(os.getenv("OPENAI_TIMEOUT", "60")),
        )
        return invoke_json_chain(llm, prompt)
    except Exception as exc:
        LAST_LLM_DEBUG = {
            "stage": "build_openai_model",
            "error_type": type(exc).__name__,
            "error_message": str(exc),
            "raw_text_preview": "",
            "json_found": False,
        }
        return None


def optimize_return_under_risk(
    prices: pd.DataFrame,
    min_assets: int = 3,
    max_assets: int = 10,
    max_sigma_ann: float = 0.35,
    rolling_window: int = DEFAULT_ROLLING_WINDOW,
    trading_days: int = DEFAULT_TRADING_DAYS,
    initial_random_samples: int = 10,
    iterations: int = 15,
    candidates_per_iter: int = 5,
    seed: int = 42,
    model: str | None = None,
    base_url: str | None = None,
    temperature: float = 1.0,
    prompt_out: str | Path | None = None,
    verbose: bool = False,
    llm_attempts_per_iteration: int = 2,
    progress: bool = False,
) -> tuple[WeightedPortfolioEvaluation, pd.DataFrame]:
    rng = np.random.default_rng(seed)
    model = model or os.getenv("OPENAI_MODEL", DEFAULT_LLM_MODEL)
    base_url = base_url or os.getenv("OPENAI_BASE_URL", DEFAULT_LLM_BASE_URL)
    context = build_rolling_return_risk_context(prices, window=rolling_window, trading_days=trading_days)
    available_assets = context.tickers

    rows = []
    epoch_rows = []
    previous: list[dict] = []
    seen: set[tuple[tuple[str, float], ...]] = set()
    best: WeightedPortfolioEvaluation | None = None

    def evaluate_and_store(iteration: int, source: str, weights: dict[str, float]) -> bool:
        nonlocal best
        key = weight_signature(weights)
        if key in seen:
            return False
        seen.add(key)
        ev = evaluate_weighted_portfolio(weights, context, max_sigma_ann, min_assets, max_assets)
        row = {
            "iteration": iteration,
            "source": source,
            "score": ev.score,
            "weights": json.dumps(ev.weights, ensure_ascii=False),
        }
        row.update(ev.metrics)
        rows.append(row)
        previous.append({"weights": ev.weights, "score": ev.score, "metrics": ev.metrics})
        if best is None or ev.score > best.score:
            best = ev
        if verbose:
            best_score = best.score if best is not None else float("nan")
            print(
                f"iter={iteration:>2} source={source:<4} "
                f"score={ev.score:.4f} best={best_score:.4f} "
                f"mu={ev.metrics.get('mu_ann', float('nan')):.4f} "
                f"sigma_q0.9={ev.metrics.get('sigma_q_0.9', float('nan')):.4f} "
                f"risk_violation={ev.metrics.get('risk_violation', float('nan')):.4f} "
                f"n_assets={ev.metrics.get('n_assets', 0)} "
                f"weights={format_weights_compact(ev.weights)}",
                flush=True,
            )
        return True

    for _ in range(initial_random_samples):
        evaluate_and_store(-1, "seed", random_weighted_portfolio(rng, available_assets, min_assets, max_assets))

    for iteration in range(iterations):
        prompt_language = "ru" if is_gigachat_model(model) else "en"
        evaluated_this_iter = 0
        best_before_iter = best.score if best is not None else float("nan")
        if progress:
            print(f"iter={iteration + 1}/{iterations} requesting candidates...", flush=True)
        for llm_attempt in range(max(1, llm_attempts_per_iteration)):
            prompt = build_prompt(
                available_assets,
                previous,
                candidates_per_iter - evaluated_this_iter,
                min_assets,
                max_assets,
                max_sigma_ann,
                iteration,
                language=prompt_language,
                retry=llm_attempt,
            )
            if prompt_out:
                Path(prompt_out).parent.mkdir(parents=True, exist_ok=True)
                Path(prompt_out).write_text(prompt, encoding="utf-8")
            payload = call_langchain(prompt, model, base_url, temperature)
            llm_debug = dict(LAST_LLM_DEBUG)
            parsed_batch = parse_candidate_batch(payload) if payload else None
            raw_candidates_count = len(parsed_batch.candidates) if parsed_batch else 0
            candidates = parse_weighted_candidates(parsed_batch, available_assets, min_assets, max_assets) if parsed_batch else []
            source = "llm" if llm_attempt == 0 else "llm_retry"
            accepted_this_attempt = 0
            for weights in candidates:
                if evaluated_this_iter >= candidates_per_iter:
                    break
                if evaluate_and_store(iteration, source, weights):
                    evaluated_this_iter += 1
                    accepted_this_attempt += 1
            best_score = best.score if best is not None else float("nan")
            epoch_rows.append(
                {
                    "iteration": iteration,
                    "attempt": llm_attempt + 1,
                    "source": source,
                    "requested_candidates": candidates_per_iter,
                    "raw_candidates": raw_candidates_count,
                    "parsed_candidates": len(candidates),
                    "accepted_candidates": accepted_this_attempt,
                    "accepted_total_iteration": evaluated_this_iter,
                    "llm_error_stage": llm_debug.get("stage", ""),
                    "llm_error_type": llm_debug.get("error_type", ""),
                    "llm_error_message": llm_debug.get("error_message", ""),
                    "llm_json_found": llm_debug.get("json_found", False),
                    "llm_raw_preview": llm_debug.get("raw_text_preview", ""),
                    "best_score_before": best_before_iter,
                    "best_score_after": best_score,
                    "improved": bool(best_score > best_before_iter) if not np.isnan(best_before_iter) else bool(accepted_this_attempt),
                }
            )
            if evaluated_this_iter >= candidates_per_iter:
                break
            if verbose:
                print(
                    f"iter={iteration:>2} source=retry "
                    f"valid={evaluated_this_iter}/{candidates_per_iter} "
                    f"raw={raw_candidates_count} parsed={len(candidates)} "
                    f"stage={llm_debug.get('stage', '')} "
                    f"err={llm_debug.get('error_type', '')} "
                    f"attempt={llm_attempt + 1}/{max(1, llm_attempts_per_iteration)}"
                )
        if progress:
            best_score = best.score if best is not None else float("nan")
            print(
                f"iter={iteration + 1}/{iterations} accepted={evaluated_this_iter}/{candidates_per_iter} "
                f"best={best_score:.4f}",
                flush=True,
            )

    if best is None:
        raise ValueError("No weighted portfolios were evaluated")
    history = pd.DataFrame(rows)
    if epoch_rows:
        history.attrs["epoch_log"] = pd.DataFrame(epoch_rows)
    return best, history


