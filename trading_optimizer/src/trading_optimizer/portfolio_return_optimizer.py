@dataclass(frozen=True)
class WeightedPortfolioEvaluation:
    weights: dict[str, float]
    score: float
    metrics: dict[str, float]


@dataclass(frozen=True)
class RollingReturnRiskContext:
    tickers: list[str]
    logret_mat: np.ndarray
    cov_tensor: np.ndarray
    window: int
    trading_days: int


class PortfolioCandidate(BaseModel):
    model_config = ConfigDict(extra="ignore")

    weights: dict[str, float] = Field(
        description="Portfolio weights by ticker. Keys are tickers from available_assets, values are positive numbers from 0 to 1."
    )

    @field_validator("weights", mode="before")
    @classmethod
    def parse_weights(cls, value: Any) -> dict[str, float]:
        if not isinstance(value, dict):
            raise ValueError("weights must be an object/dict")
        parsed: dict[str, float] = {}
        for asset, weight in value.items():
            try:
                if isinstance(weight, str):
                    cleaned = weight.strip().replace("%", "")
                    parsed_weight = float(cleaned)
                    if parsed_weight > 1:
                        parsed_weight = parsed_weight / 100.0
                else:
                    parsed_weight = float(weight)
            except (TypeError, ValueError):
                continue
            if parsed_weight > 0:
                parsed[str(asset)] = parsed_weight
        if not parsed:
            raise ValueError("weights must contain at least one positive numeric value")
        return parsed


